public class BoxingWeight {
    public static void main(String[] args){
        var boxingWeight1 = 78.2;
        var boxingWeight2 = 82.7;
        System.out.println("Общий вес: " + (boxingWeight1+boxingWeight2));
        System.out.println("Разница в весе: " + (boxingWeight2-boxingWeight1));
        System.out.println("Разница в весе: " + (boxingWeight2%boxingWeight1));
    }
}
